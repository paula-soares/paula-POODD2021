// a estrutura do for é : inicialização, condição e incremento
// onde voce atribuir um valor inicial a uma variavel, depois da a condição pra o codigo rodar ate
//certo numero e o  incremento é sobre como essa condição vai rodar, geralmente adicionando um valor de 1 a variavel 
// que é i = i + 1 ou i++

for (let i = 0; i<20 ; i=i+1){
    console.log(i);
}

for (let a = 0; a<20 ; a++){
    console.log(a);
}

for (let b = 10; b<50 ;b=b+5){
    console.log(b);
}

